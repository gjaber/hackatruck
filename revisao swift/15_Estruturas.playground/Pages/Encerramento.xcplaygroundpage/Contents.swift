 ## Encerramento
 
Todos os apps têm um modelo que representa os dados que usa.
 
Na linguagem Swift, você pode definir tipos para organizar os dados de uma forma que faça sentido para o seu app.
 
Um jeito de criar tipos personalizados é definindo estruturas.

Uma estrutura é um tipo que pode agrupar propriedades e métodos.
 
Algumas propriedades contêm dados, como variáveis ou constantes. Outras retornam um valor calculado.

Você pode criar e usar instâncias de tipos personalizados da mesma forma que qualquer outro tipo na linguagem Swift.
 
Você pode usar os tipos personalizados como qualquer outro tipo, inclusive como parâmetros de função e tipos de retorno.

