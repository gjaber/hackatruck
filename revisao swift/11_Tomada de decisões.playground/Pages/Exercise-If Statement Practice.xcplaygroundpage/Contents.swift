    print ("a e c sao iguais")
}


// Se a é menor que b, imprimir "b é melhor que a"
if a < b {
    print ("b e melhor que a")
}


// Se b é maior que a, imprimir "a não vai ganhar de b"
if (b > a) {
    print ("a nao vai ganhar de b")
}


// Se a é menor ou igual a c, imprimir "a vai perder ou empatar com c"
