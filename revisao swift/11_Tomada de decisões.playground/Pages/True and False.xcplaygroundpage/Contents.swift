 ## Verdadeiro ou falso
 
 No jogo 20 perguntas, é possível identificar qualquer coisa do universo fazendo perguntas que podem ser respondidas com *sim* ou *não*. As perguntas com respostas simples podem ser muito poderosas.
 
 Você pode escrever códigos que podem ser vistos como perguntas, e a linguagem Swift dirá se a resposta é _verdadeira_ ou _falsa_. Os resultados aparecem na barra lateral, como quando você inseriu cálculos.
 
 Na verdade, `true` e `false` são valores especiais na linguagem Swift, como você pode ver:
*/
