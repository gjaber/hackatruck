/*:
 ## Exercício – Instrução “else” na prática
 
 Você se lembra de que as instruções `else` contêm códigos executados quando o resultado de uma instrução `if` não é true. Elas podem reduzir os códigos necessários e esclarecer o fluxo de um programa.
*/
let secretWord = "banana"
let guess = "maçã"

if secretWord == guess {
    print("Acertou.")
} else  {
    print("Errou.")
}
