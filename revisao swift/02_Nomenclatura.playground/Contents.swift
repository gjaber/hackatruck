//: #### Problema animal
//: Imagine que um amigo está organizando uma exposição de animais e precisa saber quantos animais de cada espécie os participantes vão levar.
//: Ele também precisa saber qual é o total de animais e quantos deles são mamíferos.
//:
//: Digamos que você só tem um laptop com o Xcode Playgrounds instalado. Talvez possamos ajudar seu amigo.
//:
//: Usando o que vimos até agora, você poderia contar cada espécie usando um comentário e um número, assim:

// Number Of Dogs (Número de cachorros)
5

// Number Of Cats (Número de gatos)
4

// Number Of Turtles (Número de tartarugas)
3

//: Depois, você poderia copiar esses números e deixar que o Playground faça os cálculos, assim:

// Total Number Of Animals (Total de animais)
5 + 4 + 3

// Total Number Of Mammals (Total de mamíferos)
5 + 4

//: - experiment:\
//:(Experiência):\
//:Durante a semana, você recebe mais informações sobre os animais. Atualize o código acima com as alterações.
//: 
//: Por exemplo, se mais uma pessoa for levar um cachorro, altere `Number Of Dogs` de `5` para `6`, e troque o `5` por `6` em todos os lugares que ele aparecer (`Total Number Of Animals` e `Total Number Of Mammals`).
