//: ## Problema animal
//: Imagine que um amigo está organizando uma exposição de animais e precisa contar quantos animais de cada espécie estarão presentes.
//: Seu amigo também precisa saber quantos animais no total e quantos mamíferos estarão no evento.
//:
//: Agora imagine que você só tem um laptop com o Xcode instalado. Talvez você possa usar um playground para ajudar o seu amigo.
//:
//: Usando o que aprendeu até agora, você poderia contar cada espécie usando um comentário e um número, assim:
// Number Of Dogs (Número de cachorros)
5

// Number Of Cats (Número de gatos)
4

// Number Of Turtles (Número de tartarugas)
3
//: Depois, você poderia copiar esses números para novas linhas de código e deixar que o playground faça os cálculos, assim:
// Total Number Of Animals (Total de animais)
5 + 4 + 3

// Total Number Of Mammals (Total de mamíferos)
5 + 4
//: - experiment:\
//:(Experiência):\
//:A semana vai passando, e você recebe mais informações sobre os animais que vão participar do evento.\
//:Altere o código acima para atualizar os números.
//:
//: Por exemplo, se mais uma pessoa for levar um cachorro, altere `Number Of Dogs` de `5` para `6` e troque o `5` por `6` em todos os lugares que ele aparecer (`Total Number Of Animals` e `Total Number Of Mammals`).
//:
//: Se alguém for levar outra espécie, use o mesmo padrão utilizado para contar os outros animais. Adicione um novo comentário (`// Number Of Hamsters`, por exemplo) em uma linha, com um número na linha seguinte. Não se esqueça de atualizar o total.
//:
//: - Mais uma pessoa vai levar um cachorro.
//: - Uma das tartarugas está doente e não vai poder participar.
//: - Mais alguém vai levar um gato.
//: - Alguém vai levar um hamster (mamífero).
//:
//:  Será que existe um jeito melhor de resolver esse problema? Pense nisso durante alguns minutos, dê continuidade ao curso.
