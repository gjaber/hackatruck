//: ## Papo de programador
//: Ser desenvolvedor de apps é muito mais que saber o que digitar no Xcode. Os programadores trabalham em equipe, conversam sobre como resolver problemas e compartilham explicações. Por isso, é importante conhecer a terminologia usada por eles.
//:
//: Neste curso, além de aprender a programar e a criar apps, você vai ficar por dentro dos termos necessários para conversar sobre esse assunto com outros desenvolvedores.
//:
//: Por exemplo, o nome mais formal da “linha de código” é _instrução_.
//:
//: Você já viu os termos _constante_ e _identificador_ neste playground. Confira a instrução abaixo para entender mais alguns termos.
let numberOfDogs = 101
//: A definição de uma constante em linguagem Swift é chamada de _declaração_ de constante. Portanto, a instrução acima é conhecida como _declaração_. “Declaro que o nome `numberOfDogs` tem o valor `101`!”.
//:
//: A palavra `let` é um exemplo de _palavra-chave_. As palavras-chave têm um significado especial na linguagem Swift e não podem ser usadas como nomes. A palavra-chave `let` é usada para declarar uma constante. Com o tempo, você vai conhecer mais palavras-chave da linguagem Swift.
//:
//: A parte da instrução `= 101` é onde você _atribui_ um valor à constante. Essa ação se chama _atribuição_, e o sinal de igual `=` se chama _operador de atribuição_.
//:
//: - note:\
//:(Nota):\
//:Evite estresse e não se preocupe muito em memorizar todos os termos novos imediatamente. Você verá os termos usados em contexto ao longo de todo o curso e, se precisar refrescar a memória, basta consultar o Glossário do Guia do aluno.
