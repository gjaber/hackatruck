//:(Por que não?):\
//:Por que não usar emojis como nomes? Um motivo é que pode ser difícil diferenciar os emojis rapidamente. Outro é que talvez não haja emojis para todos os casos. E se alguém levasse uma tarântula para o evento? Não existe um emoji correspondente. As palavras são muito mais flexíveis e confiáveis.
