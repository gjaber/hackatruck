//: ## Como escolher bons nomes
//: Escolher bons nomes é uma parte muito importante da programação. Os programadores gostam de escolher bons nomes porque, embora seja importante saber como formular códigos, é ainda mais importante poder ler e entender esses códigos.
//:
//: Até agora, você usou nomes como `numberOfDogs` e `numberOfCats`. Eles são bons porque descrevem claramente o que representam.
//:
//: Imagine se esses nomes fossem `nd` e `nc` (para **n**umber of **d**ogs e **n**umber of **c**ats).\
//: Ou nomes ainda menos descritivos, como `n1` e `n2`.
//:
//: A linguagem Swift aceita nomes assim, mas imagine se você precisasse ler esses códigos depois de voltar das férias. Você se lembraria do que significa `nd` ou `n1`? E se você desse o playground para outros programadores, como eles descobririam o significado desses nomes?
