//: ## Digitação de nomes e preenchimento automático
//: O Xcode salva todos os nomes que você define. Então, quando você começa a digitar, ele exibe um menu de nomes sugeridos (os nomes que ele conhece). Essa função se chama _preenchimento automático_.
//:
//: O preenchimento automático ajuda a escrever mais rápido e com menos erros de digitação. Quanto mais você digita, melhor o menu de preenchimento automático consegue detectar o que você está querendo dizer.
//:
//: Sempre que o menu de preenchimento automático aparece, você pode:
//: - Continuar digitando para refinar a lista.
//: - Usar as setas para cima e para baixo para percorrer a lista.
//: - Pressionar a tecla Return para que o Xcode preencha o nome selecionado.
//:
//: ![Menu pop-up de preenchimento automático](autocomplete.png)
//: A lista contém nomes que você não definiu. Tudo bem ignorá-los.
//:
//: Na experiência da próxima página, você vai digitar alguns nomes e ver o preenchimento automático em ação.
