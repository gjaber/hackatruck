 ## Algoritmos
 
 Nesta aula, você combinou passos em funções e criou várias coreografias para o BoogieBot. Um cientista da computação diria que você passou esta aula desenvolvendo _algoritmos_.
 
Ao desenvolver um algoritmo, você define uma série de etapas que devem ser realizadas. Não precisam ser passos de dança, é claro. Qualquer série de operações passo a passo é um algoritmo.
 
A definição de algoritmos é uma grande parte da programação. Existem algoritmos matemáticos complexos que servem, por exemplo, para compactar vídeos e facilitar sua transmissão pela Internet. Também existem outros que não têm nada a ver com matemática, como um algoritmo que verifica se o usuário de um app tem mensagens novas e exibe notificações. E, é claro, existem algoritmos para fazer robôs de software dançarem.
 
