/*:
 ## Coreografias
 
 Dançar é divertido, mas se você escrever todos os passos um por um, até a descrição de uma dança simples vai ficar longa e repetitiva. Em vez disso, as pessoas sequenciam os passos e depois dão um nome à coreografia. 
 
 Em programação, é possível criar coreografias com funções assim:
 */
func doTheDisco() {
    leftArmUp()
    shakeItRight()
    leftArmDown()
    shakeItCenter()
}
