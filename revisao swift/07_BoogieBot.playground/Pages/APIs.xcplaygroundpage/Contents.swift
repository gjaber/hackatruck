 ## APIs
 
 Os desenvolvedores de apps sempre trabalham com códigos fornecidos por outras pessoas. No caso do BoogieBot, várias funções são definidas para fazer com que ele seja iniciado, se movimente e mude de cor.
 
 O conjunto de funções que controlam o BoogieBot é um exemplo de _Interface de Programação de Aplicativos_, mais conhecida pela sigla em inglês de _Application Programming Interface, API_.
 
 Uma API é um conjunto específico de recursos que pode ser usado por um desenvolvedor de software para realizar uma tarefa. Nesse caso, a API do BoogieBot faz um robô de software dançar.
 
  ### API do BoogieBot
 Inicie o BoogieBot:
 - `startBot()`
 
 Movimente o BoogieBot:
 
 - `leftArmUp()`, `leftArmDown()`, `rightArmUp()`, `rightArmDown()`
 - `leftLegUp()`, `leftLegDown()`, `rightLegUp()`, `rightLegDown()`
 - `shakeItLeft()`, `shakeItRight()`, `shakeItCenter()`
 - `jumpUp()`, `jumpDown()`
 
 Mude a cor do BoogieBot:
 - `fabulize()`, `defabulize()`
 
 A seguir, aprenda a juntar essas funções para criar coreografias.
 
