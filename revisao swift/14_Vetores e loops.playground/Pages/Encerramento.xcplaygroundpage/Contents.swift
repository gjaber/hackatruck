 ## Encerramento
 
 Os vetores podem ser usados para conter listas de itens. Os vetores têm duas características principais:
 
 - Os itens de um vetor são todos do mesmo tipo.
 - Os itens de um vetor ficam em uma ordem específica.
 
 Graças a essas duas características, é possível acessar os itens a partir de pontos específicos no vetor usando o índice, e você sempre recebe um valor de um tipo conhecido.
 
 Algumas outras coisas que você aprendeu sobre vetores:
 
 - O primeiro índice de um vetor é zero, não um.
 - Usar o índice para acessar um vetor pode ser perigoso. Se o índice for usado fora dos limites do vetor, o programa vai travar.
 - É possível descobrir o número de itens de um vetor usando a propriedade `count`.
 - É possível usar loops `for…in` para acessar com segurança cada item do vetor em ordem, sem precisar saber quantos itens ele contém.
 - Nos vetores mutáveis, é possível adicionar, remover e substituir itens.
 
