 ## Listas
 
 As listas fazem parte do dia a dia das pessoas, por exemplo, listas de tarefas, de desejos, playlists do iTunes e até listas de coisas para fazer antes de morrer. Elas são muito úteis e também são comuns na programação.
 
 Quando aprendeu sobre strings, você criou listas de coisas em alguns exercícios. Para fazer essas listas, você combinou várias strings usando o caractere de linha nova `\n`.
 
 ```
(Exemplo):
 let shoppingList = "Ovos" + "\n" + "Tomate" + "\n" + ...
 ```
 
 Esse código retorna uma lista fácil de ler na tela, mas não oferece mais nada. Pense em outras coisas que você poderia querer fazer com uma lista:

 - Como você poderia invocar uma função para cada item da lista sem precisar digitar todos eles novamente?
 - Como você poderia verificar se um item já foi adicionado à lista?
 - Se a lista chegasse a centenas de itens, como você poderia remover o item “Tomate” de um jeito fácil?
 - E se a lista não for formada por valores de `String`, mas sim por outros itens, como uma lista de preços que você precisa somar?
 - Qual é o primeiro item? O último item? O 24° item?
 - Quantos itens estão na lista?
 - Como é possível reorganizar a lista?

 Na linguagem Swift, as listas são chamadas de _vetores_.
 
  Pense em qualquer app para iOS em que você já tenha visto uma lista de itens. Com certeza, esse app usou um vetor.
  
 Nesta aula, você vai aprender como criar e trabalhar com vetores.

