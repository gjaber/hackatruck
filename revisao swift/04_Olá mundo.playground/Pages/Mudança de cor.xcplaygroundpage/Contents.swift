 ## Mudança de cor

Muitos programadores gostam de configurar seus ambientes de computação com um estilo que os deixe mais à vontade. Para fazer mudanças no ambiente do Xcode, selecione Xcode > Preferences… (Xcode > Preferências...) na barra de menus:

 ![Barra de menus com o menu “Xcode” aberto ](xcode-preferences.png)

Na janela Preferences (Preferências), escolha a aba Fonts &amp; Colors (Fonte e cores) e selecione Source Editor (Editor de fonte):

 ![Painel de preferências do Xcode mostrando a aba Fonts and Colors (Fontes e cores)](xcode-pref-fonts-and-colors.png)


Para deixar o visual bem retrô, escolha uma das três opções com fundo escuro: Civic (Cívico), Dusk (Crepúsculo) ou Midnight (Meia-noite):

 ![Painel de preferências do Xcode mostrando Fonts and Colors (Fontes e cores) com a opção Dusk (Crepúsculo) selecionada](xcode-pref-dusk-color.png)


Com essa mudança ficou difícil de ler o código? Não tem problema. Essas preferências são _suas_, e você pode deixar o visual do código do jeito que quiser. Se você consegue ler o código com esse fundo escuro, fique com ele nas próximas páginas do playground, pois vamos ensinar como adicionar outros efeitos vintage.
 
