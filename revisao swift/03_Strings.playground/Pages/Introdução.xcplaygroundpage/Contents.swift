//: ## Strings
//: Quase todos os apps têm texto: apps de mensagens, leitores de notícias, redes sociais... Até os apps de câmera podem ter texto para identificar botões e controles.
//:
//: Na maioria das linguagens de programação, incluindo a Swift, os valores de texto são chamados de _strings_.
//:
//: ### Por que strings?
//: Imagine um colar de miçangas. Cada miçanga pode ser diferente, mas todas estão unidas em uma ordem determinada.
//:
//: ![colar de miçangas](StringOfBeads.png)
//: Normalmente, uma letra solta (por exemplo, “H”) não serve para nada. As palavras são formadas por sequências de letras, como um colar de miçangas. Por isso, os programadores chamam os textos de _string_, que significa “sequência” em inglês. Uma string pode ser qualquer texto que você imaginar.
//:
//: ![sequência de caracteres](StringOfCharacters.png)
//:
//: A seguir, veja o que mais, além de letras, pode formar uma string.
