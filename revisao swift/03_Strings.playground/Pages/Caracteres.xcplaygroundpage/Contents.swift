//: ## Caracteres
//: É claro que o texto pode conter mais que letras. Tem sinais de pontuação, números, símbolos e até espaços. Todas essas coisas são chamadas de _caracteres_, e as _strings_ são formadas por esses _caracteres_.
//:
//: As letras são caracteres:
//: ## Aa Bb Cc Dd Ee
//: Os sinais de pontuação também:
//: ## ! ? . , ; :
//: Números:
//: ## 1 2 3 4 5 6 7 8 9 0
//: Símbolos:
//: ## # @ $ & © π * + - /
//: Até caracteres invisíveis, como espaços e tabulação:\
//: _(Não dá para ver, mas eles estão aí)_
//: ## `                         `
//:
//:
//: Agora que você já conhece as peças, é hora de montar strings.\
