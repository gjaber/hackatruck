//: ## Strings
//: Abra um app qualquer e com certeza você verá algum texto. O texto está em toda parte: apps de mensagens, de notícias, de redes sociais... Até os apps de câmera usam texto nos botões e controles. Na ciência da computação, os valores de texto quase sempre são chamados de _strings_.
//:
//: ### Por que strings?
//: Imagine um colar de miçangas. Cada miçanga pode ter um visual ou um formato diferente, mas todas estão unidas em uma ordem determinada.
//: ![colar de miçangas](StringOfBeads.png)
//: Normalmente, um caractere solto, por exemplo a letra “H”, não é muito útil. Precisamos de uma sequência de caracteres para formar palavras, frases, parágrafos e assim por diante. Por isso, os programadores chamam os textos de _string_, que significa “sequência” em inglês.
//: ![sequência de caracteres](StringOfCharacters.png)
