/*:
 Os valores que não podem ser alterados são chamados de **constantes**, pois o valor deles é constante. Ao usar `let` para declarar um valor, você está dizendo que esse valor nunca mudará.
 
 Isso é muito útil. Às vezes, você não quer que os valores mudem ou que sejam alterados sem querer. Mas outras vezes você _quer_ poder fazer alterações. Nesse caso, você precisa de **variáveis**, não constantes. 
