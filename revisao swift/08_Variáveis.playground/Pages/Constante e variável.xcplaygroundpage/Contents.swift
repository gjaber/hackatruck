 ## Constante e variável
 
Em programação, existe uma diferença importante entre os nomes que sempre se referem ao mesmo valor e os nomes que mudam de valores ao longo do tempo.
 
 Quando você escolhe os nomes das coisas no mundo real, essa diferença também existe, mas as pessoas normalmente não percebem.
 
 #### Constante
 
 Pense, por exemplo, na “sua data de nascimento”. Ela nunca vai mudar. Não importa qual seja a sua idade, a data continua a mesma. Mesmo que passem centenas de anos, quando as pessoas lerem sobre como você mudou o mundo com seus apps incríveis, sua data de nascimento continuará sendo a mesma.
 
 Sua data de nascimento é uma constante. O valor é definido quando você nasce e depois nunca mais muda. Na programação, `birthdate` seria uma constante.
 
 
 #### Variável
 
 Muitos dos nomes que as pessoas usam todos os dias podem ter diferentes valores ao longo do tempo.
 
 Por exemplo, pense na “sua camiseta preferida” ou em outra peça de roupa. Quando criança, você tinha uma camiseta preferida. Conforme você foi ficando mais velho, essa camiseta deixou de servir ou seu gosto mudou, e agora você prefere outra. No futuro, provavelmente sua camiseta preferida será outra diferente. A ideia de “camiseta preferida” continua existindo, mas a camiseta a que essa ideia se refere muda ao longo do tempo. Em programação, você diria que `favoriteShirt` é uma variável.
 
 #### Um pouquinho de cada
 Você pode ver um pouco das ideias de constantes e variáveis na maioria dos esportes de equipe. Existe o conceito de “time da casa” e “visitante”. Na escola, o time da casa é sempre o mesmo. Para sua escola, ele é uma constante. O nome ou conceito de “visitante” continua o mesmo, mas o time muda a cada jogo. O time visitante é uma variável.
 
