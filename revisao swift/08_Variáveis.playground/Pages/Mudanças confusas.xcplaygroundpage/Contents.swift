 ## Mudanças confusas
 
 Você já deve ter ficado confuso com alguma mudança.
 
 Imagine que você vai encontrar um grupo de seis amigos no cinema às 20h.
 
 Então, você recebe uma mensagem avisando que houve uma mudança de planos, que agora vocês vão assistir a outro filme em outro cinema e vão se encontrar às 21h.
 
 Um pouco mais tarde, você recebe outra mensagem dizendo que os planos mudaram mais uma vez. Agora vocês vão assistir a um filme na casa de um dos amigos. O horário mudou para 18h, e vocês vão pedir pizza.
 
 Depois, você recebe outra mensagem, dizendo que vocês vão se reunir na casa de outro amigo, às 18h30.
 
 - experiment:\
(Experiência):\
Com todas essas mudanças, qual é a probabilidade de que todos estejam no lugar certo na hora certa?\
\
Seria menos provável que houvesse erros se os planos não tivessem mudado?
 
 Sempre que você faz uma mudança, existe a probabilidade de que alguém acabe usando a informação antiga e cometendo um erro.
 
 Isso também acontece na programação. Usando uma constante, você garante que um valor nunca vai mudar. Não existe a possibilidade de que outra parte do programa fique dessincronizada ou use um valor antigo.
 
 Mas, às vezes, é necessário usar variáveis. As coisas mudam durante a execução de um programa. Em programação, os objetos que têm capacidade de mudar são chamados de _mutáveis_, palavra que vem do latim _mutare_, que significa “transformar-se”.
 
 Já os objetos que não têm capacidade de mudar são chamados de _imutáveis_. Como regra prática, se não for necessário que um objeto mude, use sempre uma constante para reduzir as possibilidades de confusão e erros.
 
 Falando em erros, continue para conhecer um erro comum durante o trabalho com constantes e variáveis.
