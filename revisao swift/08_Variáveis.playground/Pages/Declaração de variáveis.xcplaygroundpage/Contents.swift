/*:
 ## Declaração de variáveis
 
 Declarar uma variável é quase exatamente igual a declarar uma constante, mas, em vez de usar a palavra-chave `let`, você usa a palavra-chave `var`:
 */
// Para declarar uma constante
let placeOfBirth = "Nova Jersey"

// Para declarar uma variável
var currentLocation = "Nova Jersey"

/*:
 Depois de declarar uma variável, você pode atribuir um novo valor a ela:
*/
// Para atribuir um novo valor a uma variável
currentLocation = "Califórnia"

/*:
 - experiment:\
(Experiência):\
Abaixo, adicione uma linha de código que atribua um novo valor a `currentLocation`:
 */
// Atribua um novo valor
currentLocation = "Campo Grande"
/*:
 As constantes e variáveis têm muitas semelhanças:
- As duas usam as mesmas regras para nomes
- As duas associam um nome a um valor atribuído
- As duas têm um tipo específico
 
 A diferença é:
 - O valor de uma constante *não pode* mais ser alterado depois que é atribuído
 - O valor de uma variável *pode* ser alterado depois que é atribuído

A seguir, aprenda a trabalhar com variáveis.
 */
