/*:
 Você também aprendeu a definir um nome e a associá-lo a uma lista de instruções declarando uma função:
 */
func printGreeting() {
    let greeting = "Olá"
    print(greeting)
}
/*:
Você já deve ter começado a perceber um padrão. Grande parte da programação é criar coisas, dar nome a elas e depois chamar essas coisas pelo nome para usá-las.
 
 Até agora, você usou constantes sempre que precisou atribuir valor a um nome. Mas ainda não respondemos a uma pergunta muito importante: por que elas se chamam constantes?
 
 Talvez você já tenha adivinhado que o nome “constante” tem a ver com coisas que permanecem iguais (ou seja, constantes) ao longo do tempo.
 
 Assim que o valor de uma constante é atribuído, você pode ter certeza de que ele vai continuar sempre o mesmo. O valor associado a esse nome permanece constante.
 
 Existe outra maneira de associar um valor a um nome na linguagem Swift. São as _variáveis_.
 
 Com uma variável, é possível alterar o valor durante a execução do programa. Ou seja, o valor associado ao nome pode mudar (variar) ao longo do tempo.
 
 Neste playground, você vai aprender mais sobre as diferenças entre variáveis e constantes, como declarar variáveis e como decidir se deve usar uma variável em vez de uma constante.
 
 Como tudo isso funciona e por que é importante?
 
 Passe para a próxima página para saber mais.

 
 */
