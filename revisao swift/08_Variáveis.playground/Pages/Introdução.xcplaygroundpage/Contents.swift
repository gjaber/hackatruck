/*:
 ## Dar nome aos bois (de novo)
 
Nos playgrounds _Nomenclatura e identificadores_, você viu a importância de dar nomes às coisas na vida real e na programação.
 
No código, você aprendeu a definir um nome e a associar um valor a ele declarando uma constante:
 */
