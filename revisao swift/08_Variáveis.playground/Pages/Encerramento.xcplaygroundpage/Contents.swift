 ## Encerramento
 
 Neste playground, você aprendeu alguns conceitos importantes:
 
 - Os valores declarados com `let` são constantes e não podem ser alterados depois da atribuição. Esses valores são chamados de _imutáveis_.
 - Os valores declarados com `var` são variáveis e podem ser alterados ao longo do tempo. Esses valores são chamados de _mutáveis_.
 - Um valor mutável pode ser usado dentro de uma instrução de atribuição para si mesmo: `score = score + 10`.
 - Os operadores de atribuição composta permitem a atualização de valores mutáveis: `score += 10`.
 - Usar constantes e variáveis nos lugares certos ajuda a deixar seu código mais seguro e mais fácil de entender.
 
