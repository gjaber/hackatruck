 - O que é um valor.
 - O que um valor pode fazer.
 - Onde é possível usar um valor.
 
 Todas as linhas de código que você formula são verificadas para evitar o uso incorreto de tipos.
 
 A linguagem Swift pode deduzir o tipo de uma constante ou variável por meio do valor atribuído a ela.
 
