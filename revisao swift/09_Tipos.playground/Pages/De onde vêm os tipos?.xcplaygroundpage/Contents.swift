 ## De onde vêm os tipos? 
 
 A linguagem Swift tem tipos integrados que representam os componentes básicos de todos os programas. Você passou muito tempo vendo `String` e `Int`, mas existem muitos outros.
 
 Eles fazem parte da _biblioteca padrão da linguagem Swift_. Todas as linguagens de programação têm algo assim: o conjunto básico de recursos necessários para realizar tarefas de programação essenciais.
 
 Você passou muito tempo vendo os tipos `String` e `Int`, mas existem muitos outros na biblioteca padrão da linguagem Swift, como os exemplos abaixo. Você não precisa se preocupar com o significado deles por enquanto, mas pode tentar adivinhar:
 
 - `Array`
 - `Dictionary`
 - `Set`
 - `Sequence`
 - `Error`
 - `Bool`
 
 Veja que todos os nomes de tipos começam com letra maiúscula. Se houver mais de uma palavra, a primeira letra de cada uma deve ser maiúscula. É um pouco diferente das regras para nomear constantes, variáveis e funções, que começam com letras minúsculas.
 
 Essa diferença é intencional. Como os nomes dos tipos começam com letra maiúscula, é fácil diferenciá-los dos nomes de funções, variáveis e constantes. Os dois alternam entre palavras começadas por maiúsculas e minúsculas, mas, no caso dos tipos, _a primeira palavra começa com maiúscula_ e, nos outros casos, _a primeira palavra começa com minúscula_.
 
