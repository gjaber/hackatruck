Pense em um dos tipos que você criou na experiência da página anterior e imagine de que outros tipos ele poderia depender. Por exemplo, um `TrainingShoe` (Tênis de treino) pode ter uma `Int` para o tamanho, uma `String` para a marca, uma `Date` para a data de lançamento, e mais uma `Int` para o preço em dólares.


 Os tipos e recursos podem ser agrupados em coleções chamadas de _estruturas_ ou _bibliotecas_. Quando estiver criando apps, você pode aproveitar as estruturas incluídas no Xcode. Uma estrutura muito importante é a *Foundation*.
 
 Ela introduz muitos tipos usados para representar coisas mais específicas que strings ou números da biblioteca padrão da linguagem Swift. Por exemplo, existem tipos para datas, distâncias e arquivos de um computador.
 
 Essas estruturas adicionais não são adicionadas automaticamente aos programas, pois podem não ser necessárias. Seria como fazer uma mala com todas as suas roupas para ir ao supermercado.
 
