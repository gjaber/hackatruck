/*:
 ## Tipos importantes
 
 Na vida real, é muito importante usar o tipo certo de objeto.
 
 Para jogar tênis, é importante usar uma bola e não outro tipo de coisa, por exemplo, um ovo.
 
 Por outro lado, dezenas de bolas de tênis não servem para fazer um omelete.
 
 Diferentes tipos de coisas têm diferentes propriedades e comportamentos. As bolas de tênis estão cheias de ar e quicam quando caem no chão. Os ovos têm clara e gema, e quebram quando caem no chão.
  
 Na programação, os tipos funcionam da mesma forma.
 
 Por exemplo, você já usou um tipo chamado string para trabalhar com caracteres e texto. As strings têm propriedades e comportamentos específicos para essa finalidade.
 
