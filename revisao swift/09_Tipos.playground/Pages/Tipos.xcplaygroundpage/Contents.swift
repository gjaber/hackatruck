/*:
 ## Tipos
 Na linguagem Swift, todo valor tem um _tipo_.
 
 Assim como os tipos de coisas na vida real, na linguagem Swift cada tipo tem o próprio conjunto de propriedades e comportamentos.

 Você já trabalhou com dois tipos de valores diferentes:
 
 - Strings, como `"Olá"` e `"Wimbledon"`
 - Números, como `101` e `-10`
 */
let string = "Sou uma string"
let number = 12
//: Você também já viu que esses tipos têm comportamentos diferentes. O que acontece quando você soma todos eles, por exemplo?
1 + 1 // igual a 2
"1" + "1" // igual a "11"
