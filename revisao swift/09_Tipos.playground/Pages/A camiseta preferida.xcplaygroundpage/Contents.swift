 ## A camiseta preferida
 
 Pense na sua camiseta preferida. Provavelmente, ela não é uma banana. Isso não faria sentido, pois uma banana não é uma camiseta.
 
 Por outro lado, se você pensar na sua fruta preferida, seja banana, maçã ou outra coisa, com certeza será algum tipo de fruta.
 
 Quando as pessoas dizem o nome de uma coisa — “fruta preferida”, por exemplo — esse nome costuma estar relacionado a um tipo de coisa.
 
 A ideia de “fruta preferida” só fará sentido se estiver relacionada a um tipo de fruta. Não faria sentido relacioná-la a “chave inglesa” ou “astronomia”. Seu cérebro é eficiente garantindo que os nomes tenham correspondência com os tipos de coisas corretos.
 
 Da mesma forma, a linguagem Swift monitora os tipos de valores associados a constantes e variáveis, e garante que você não transforme um valor em “banana” sem querer quando ele deveria ser “camiseta”.
 
