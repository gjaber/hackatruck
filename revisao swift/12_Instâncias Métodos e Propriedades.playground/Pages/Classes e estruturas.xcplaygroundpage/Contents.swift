 Quando estiver desenvolvendo apps em linguagem Swift, você vai trabalhar com instâncias de _structs_ (abreviação de _estruturas) e classes_. As duas são formas de definir tipos nessa linguagem.
 
 Por enquanto, você só precisa saber que as estruturas e as classes têm muitas semelhanças:
 
- As duas têm instâncias
- As instâncias são criadas com um inicializador
- As duas podem ter métodos
- As duas podem ter propriedades
 
Para criar e usar instâncias, você vai formular o mesmo código em linguagem Swift, tanto se o tipo for estrutura quanto se for classe.
 
Durante este curso, você vai praticar mais a criação de instâncias e o uso de métodos e propriedades. Em uma das aulas, você também vai aprender a definir estruturas. Não vamos explorar as diferenças entre classes e estruturas aqui, mas quando ganhar experiência no uso das duas, você estará preparado para aprender sobre essas diferenças.

