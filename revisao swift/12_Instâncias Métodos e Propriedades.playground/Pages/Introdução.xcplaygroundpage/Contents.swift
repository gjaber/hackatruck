 ## O que é instância?
 
Você já aprendeu sobre os tipos na linguagem Swift e como eles são semelhantes aos tipos de coisas do mundo real.
 
Para cada tipo do mundo real, existem muitas instâncias físicas:
 
- Para o tipo “Carro”, existem inúmeras variações, cada uma com a sua quilometragem, marca, modelo, cor, combustível, arranhões e amassados. Cada carro também tem um conjunto específico de comportamentos: como ele é ligado, o freio, a embreagem, etc.
 
 
- Para o tipo “Cidade”, cada instância tem um nome, um número de habitantes, uma localização e pontos turísticos diferentes. Além disso, cada cidade tem seus métodos para responder a emergências ou recolher o lixo, por exemplo.
 
O tipo define ou descreve as propriedades e os comportamentos de um determinado tipo de coisa, mas cada exemplo concreto de um tipo (cada carro, cada cidade) é uma instância separada e independente.
 
Imagine dois carros da mesma marca e do mesmo modelo, fabricados no mesmo ano. Um deles está acima do limite de velocidade em uma estrada, o outro está sem gasolina, parado no acostamento. O tipo é o mesmo, mas as instâncias são diferentes.
 
Agora pense em duas cidades. A localização de cada uma é diferente. Um ponto turístico de uma cidade pode ser muito movimentado, enquanto outro, em outra cidade, pode estar decadente e vazio. As duas são cidades, mas uma pode ser bem diferente da outra.
 
Em programação, você pode criar e usar diferentes instâncias de um determinado tipo. Cada instância tem o próprio conjunto de valores de propriedade e tem comportamentos independentes das outras instâncias.
 
