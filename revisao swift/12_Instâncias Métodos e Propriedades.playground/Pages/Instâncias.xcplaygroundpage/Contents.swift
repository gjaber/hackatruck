 ## Instâncias
 
 Você já trabalhou com vários tipos na linguagem Swift, por exemplo `String`. Um valor individual de `String`, como "Olá", é chamado de uma _instância_ desse tipo.
 
 - `String` é um tipo
 - `"Olá"` é uma _instância_ de uma `String`.
 
Você já vem criando instâncias dos tipos `Int` e `String` há algum tempo. Na verdade, cada valor que você cria em um programa é uma instância de um tipo.
 
As constantes `hello` e `aDifferentHello` são duas instâncias de `String` que contêm os mesmos dados.

*/
