//: ## Cálculos
//: Os playgrounds podem ser usados como calculadoras. Basta digitar o problema matemático que você quer resolver, e a barra lateral de resultados mostra a resposta ou o _resultado_.
//:
//: Você pode usar os sinais de mais `+` e menos `–` para adição e subtração, como sempre:
34 + 56 + 230

1000 - 300

//: Para multiplicação, use o asterisco `*` :
3 * 100
//: Para divisão, a barra `/` :
24 / 8
//: E para agrupar, use parêntesis `( )` :
(300 + 500 + 400) / 3
