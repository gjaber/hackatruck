## Invocação de funções
 
Provavelmente você não percebeu, mas já aproveitou a abstração e as funções quando usou `print()` para imprimir “Hello, World!” no console. Na linguagem Swift, `print()` é uma função. Quando ela é usada, é _invocada_:
*/
print("Hello, World!")
print(360)
/*:
Assim como você realiza muitas tarefas ao trocar de roupa, muitas coisas acontecem quando a função `print()` é invocada, inclusive:
- Transformar tudo o que você informa, inclusive números, em strings.
- Adicionar um caractere de nova linha, de forma que cada invocação de `print()` termine em uma linha nova.
- Fazer com que essa string apareça no console.

Nesse caso, você invoca uma função criada por outra pessoa. Você não precisa saber todos os detalhes sobre como `print()` funciona para invocá-la.

Esse é um dos principais motivos que tornam as funções tão poderosas. Com elas, é possível combinar etapas detalhadas em uma definição que pode ser usada muitas vezes.
 
