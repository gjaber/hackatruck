 ## Como esconder a complexidade
 
 Imprimir a cantiga usando funções pode parecer mais demorado e complicado que o código original na primeira página deste playground. Se você tentar pensar em todas as funções juntas, é complicado mesmo. Mas a ideia de usar funções é dividir as coisas em partes que podem ser entendidas e reutilizadas. Cada parte realiza uma tarefa clara.
 
