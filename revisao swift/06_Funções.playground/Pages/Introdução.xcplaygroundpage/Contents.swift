 ## Trocar de roupa
 
 Imagine que um amigo convida você para ir ao cinema. Você acordou tarde, então responde “Encontro você lá. Só preciso trocar de roupa”.
 
 Seu amigo sabe o que você quer dizer com “trocar de roupa”, mas ele desconhece detalhes, como a camiseta que você vai vestir.
 
 Agora pense nos detalhes de tudo o que você fez para trocar de roupa hoje: decidir o que vestir, pegar as roupas no armário, vestir cada peça.
 
 As palavras “trocar de roupa” parecem simples, mas quando pensamos em todos os detalhes envolvidos nessa tarefa, entendemos que elas representam muita complexidade.
 
 Usar uma frase como “trocar de roupa” é uma forma de falar e pensar sobre o processo de se vestir sem precisar pensar em cada detalhe específico.
 
 As pessoas fazem isso o tempo todo. Frases como “ir ao dentista”, “fazer um sanduíche” ou “tomar um café” descrevem atividades com etapas que seriam tediosas de explicar detalhadamente.
 
 A ideia de se referir a algo complexo de forma mais simples é chamada de *abstração*. A abstração é uma ideia fundamental no desenvolvimento de software. É assim que os programadores não ficam sobrecarregados com detalhes e complexidade.
 
 Nesta aula, você vai aprender a definir e usar _funções_, uma das formas mais básicas de definir a abstração na programação.
 
